#include<bits/stdc++.h>
#define mx 100010

using namespace std;

int n,q;
int arr[ mx * 3 ];

int main(){

    freopen("input.txt","r",stdin);

    while(scanf("%d",&n) && n != 0){

        scanf("%d",&q);

        for(int i = 0; i < n; i++) scanf("%d",&arr[ i ]);

        while( q-- ){

            int x,y;

            scanf("%d %d",&x,&y);


        }


    }
    return 0;
}
